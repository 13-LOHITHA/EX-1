public class Application5 {

    public static void main(String[] args) {
        int days = 365; // Change this to the desired number of days
        double speedOfLight = 299792458.0; // Speed of light in meters per second

        double distance = speedOfLight * days * 24 * 60 * 60; // Distance = speed * time

        System.out.println("Distance traveled by light in " + days + " days is " + distance + " meters.");
    }
}
    
        
